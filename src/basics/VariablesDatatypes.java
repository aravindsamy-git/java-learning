package basics;

public class VariablesDatatypes {

	public static void main(String[] args) {
		
		int x = 10;
		float y = 12.56f;
		char p = '@';
		String name = "Aravind";
		Boolean yes = true;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(p);
		System.out.println(name);
		System.out.println(yes);
		
	}

}
